	public override void Unclip()
	{
		if (ignoreClipping)
			return;

		base.Unclip();

		if (spriteText != null)
			spriteText.Unclip();

		if (aggregateLayers != null)
		{
			for (int i = 0; i < aggregateLayers.Length; ++i)
			{
				if (aggregateLayers[i] != null)
				{
					for (int j = 0; j < aggregateLayers[i].Length; ++j)
						aggregateLayers[i][j].Unclip();
				}
			}
		}

		UpdateCollider();
	}

